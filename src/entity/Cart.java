package entity;
public class Cart {
  private int cid;
  private int jid;
  private int uid;
  private int nums;
  private float price;
  private float totalprice;
  private String receivings;
  private String state1;
  private String state2;
public int getCid() {
	return cid;
}
public void setCid(int cid) {
	this.cid = cid;
}
public int getUid() {
	return uid;
}
public void setUid(int uid) {
	this.uid = uid;
}
public int getNums() {
	return nums;
}
public void setNums(int nums) {
	this.nums = nums;
}
public float getPrice() {
	return price;
}
public void setPrice(float price) {
	this.price = price;
}
public float getTotalprice() {
	return totalprice;
}
public void setTotalprice(float totalprice) {
	this.totalprice = totalprice;
}
public String getReceivings() {
	return receivings;
}
public void setReceivings(String receivings) {
	this.receivings = receivings;
}
public String getState1() {
	return state1;
}
public void setState1(String state1) {
	this.state1 = state1;
}
public String getState2() {
	return state2;
}
public void setState2(String state2) {
	this.state2 = state2;
}
public int getJid() {
	return jid;
}
public void setJid(int jid) {
	this.jid = jid;
}
}
